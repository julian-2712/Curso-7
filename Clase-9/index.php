Modularidad

Esta no es una técnica de programación, pero si es algo con lo que debemos cumplir para que a futuro se más fácil la mantenibilidad.
Este concepto aplica views, models, controllers, helpers, etc.

Debemos pensar en módulos, no en un solo archivo con todo el código.

Este hace referencia a tener pequeños archivos que al unirlos forman el sistema en si.

-----------------------

La modularidad es la base del buen código, es de lo que venimos hablando siempre, divide todo, se lo más específico posible 
y siempre tendrás un código mejor organizado, mantenible y fácil de entender.
*/